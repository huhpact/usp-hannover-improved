document.querySelectorAll('.service__card').forEach(card => {
  card.addEventListener('mouseenter', function() {
    this.style.transform = 'translateY(-10px)';
  });
  
  card.addEventListener('mouseleave', function() {
    this.style.transform = 'translateY(0)';
  });
});

window.addEventListener('scroll', () => {
  const scrolled = window.pageYOffset;
  const decorations = document.querySelectorAll('.section__decoration');
  
  decorations.forEach(decoration => {
    const rate = scrolled * -0.5;
    decoration.style.transform = `translateX(${rate}px)`;
  });
});

function initializeTextEffects() {
  const titles = document.querySelectorAll('.section__title');
  
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.style.opacity = '1';
        entry.target.style.transform = 'translateY(0)';
      }
    });
  }, {
    threshold: 0.1
  });
  
  titles.forEach(title => {
    observer.observe(title);
  });
}

document.addEventListener('DOMContentLoaded', initializeTextEffects);